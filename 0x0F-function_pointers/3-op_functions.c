#include "3-calc.h"

/**
 * op_add - a function that add two integers
 * @a: integer a
 * @b: integer b
 * Return: return result after taking operation, or exit if Error
 */

int op_add(int a, int b)
{
return (a + b);
}


/**
 * op_sub - a function that takes subtraction of two number
 * @a: integer a
 * @b: integer b
 * Return: return result after taking operation, or exit if Error
 */

int op_sub(int a, int b)
{
return (a - b);
}

/**
 * op_mul - a function that takes multiplication of two integer
 * @a: integer a
 * @b: integer b
 * Return: return result after taking operation, or exit if Error
 */

int op_mul(int a, int b)
{
return (a * b);
}

/**
 * op_div - a function that takes division of two integer
 * @a: integer a
 * @b: integer b
 * Return: return result after taking operation, or exit if Error
 */

int op_div(int a, int b)
{
return (a / b);
}

/**
 * op_mod - a function that takes modulo of two integer
 * @a: integer a
 * @b: integer b
 * Return: return result after taking operation, or exit if Error
 */

int op_mod(int a, int b)
{
return (a % b);
}
